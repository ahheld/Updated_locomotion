% --- Executes on button press in pushbutton1. % this is the "RUN ANALYSIS"
% button
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

%loop to call each genotype's name if present
data_out = {}; %this is the data structure used for analysis

for n=1:8
    try
    obj = eval(strcat('handles.group',char(64+n))); %this mess of 'obj' takes the genotype names and changes their format to be recognized by the analysis function
    obj2 = strcat(obj,'*.wmv');
    obj3 = strjoin(obj2);
    out = larval_loc_batch(obj3,strjoin(handles.groupsec),n); %uses value from seconds handle to run for a certain number of seconds
    data_out{n} = out;
    end
end

if handles.toggleA == 1
    disp('plot data');
    data_out{1};
    proximity = 2;
    lim = 100;
    points = [0, 50, 100];
    pointlabels = {'0','50','100'};
    beeswarmbar3(data_out,proximity,lim,points,pointlabels)
end

if handles.toggleB == 1
    disp('run_tukey');
    x=[];
    group = [];
    points = numel(data_out);
    for n=1:points
        x = [x;data_out{n}];
        group = [group; n*ones(numel(data_out{n}),1)];
    end
    [p,t,s]=anova1(x,group);
    [c,m,h,nms]=multcompare(s,'alpha',.05);
end


